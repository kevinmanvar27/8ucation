// Backward-compatible SimpleSelect component for legacy API
interface SimpleSelectOption {
  value: string
  label: string
}

interface SimpleSelectProps {
  id?: string
  name?: string
  label?: string
  placeholder?: string
  options: SimpleSelectOption[]
  value?: string
  onChange?: (e: { target: { value: string } }) => void
  onBlur?: (event: { target: { name?: string; value?: string } }) => void
  disabled?: boolean
  required?: boolean
  className?: string
  error?: string
}

const SimpleSelect = React.forwardRef<HTMLButtonElement, SimpleSelectProps>(
  ({ id, name, label, placeholder, options, value, onChange, onBlur, disabled, required, className, error }, ref) => {
    const handleValueChange = (newValue: string) => {
      if (onChange) {
        // Simulate native event structure for backward compatibility
        onChange({ target: { value: newValue } })
      }
    }

    // Filter out options with empty values
    const validOptions = options.filter((option) => option.value !== "" && option.value !== null && option.value !== undefined);

    return (
      <div className="space-y-2">
        {label && (
          <label className="text-sm font-medium leading-none peer-disabled:cursor-not-allowed peer-disabled:opacity-70">
            {label}
            {required && <span className="text-destructive ml-1">*</span>}
          </label>
        )}
        <Select value={value} onValueChange={handleValueChange} disabled={disabled} name={name}>
          <SelectTrigger ref={ref} className={cn(className, error && "border-destructive")} onBlur={onBlur}>
            <SelectValue placeholder={placeholder} />
          </SelectTrigger>
          <SelectContent>
            {validOptions.map((option) => (
              <SelectItem key={option.value} value={option.value}>
                {option.label}
              </SelectItem>
            ))}
          </SelectContent>
        </Select>
        {error && <p className="text-sm text-destructive">{error}</p>}
      </div>
    )
  }
)
SimpleSelect.displayName = "SimpleSelect"

